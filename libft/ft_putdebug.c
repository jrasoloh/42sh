#include "libft.h"

void	ft_putdebug(const char *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == '\n')
			ft_putstr("\\n");
		else
			ft_putchar(s[i]);
		i++;
	}
}
