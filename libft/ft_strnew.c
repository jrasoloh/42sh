#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if ((str = (char*)ft_memalloc(sizeof(char) * (size + 1))) == NULL)
		return (NULL);
	str[size] = '\0';
	return (str);
}
